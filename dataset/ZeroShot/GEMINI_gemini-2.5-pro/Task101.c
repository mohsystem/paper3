#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <stdbool.h>
#include <sys/stat.h>

/**
 * @brief Creates a file, writes content, and makes it executable for the owner.
 *
 * This function uses standard C library functions and POSIX system calls. It is
 * not portable to non-POSIX compliant systems like native Windows without a
 * compatibility layer (e.g., Cygwin, MinGW with specific libraries).
 *
 * @param filename The path to the file to be created.
 * @return true on success, false on failure.
 */
bool createAndMakeExecutable(const char* filename) {
    // A simple, safe script content.
    const char* content = "#!/bin/bash\n"
                          "# A simple script generated by a C program.\n"
                          "echo \"Hello from script.sh! Execution successful.\"\n";
    
    // Step 1: Create and write to the file.
    FILE* fp = fopen(filename, "w");
    if (fp == NULL) {
        perror("Error: Failed to open file for writing");
        return false;
    }
    if (fputs(content, fp) == EOF) {
        perror("Error: Failed to write to file");
        fclose(fp); // Attempt to close the file even on write error
        return false;
    }
    fclose(fp);

    // Step 2: Make the file executable for the owner.
    struct stat st;
    if (stat(filename, &st) != 0) {
        perror("Error: Failed to get file stats");
        return false;
    }

    // Add execute permission for the user (S_IXUSR).
    if (chmod(filename, st.st_mode | S_IXUSR) != 0) {
        perror("Error: Failed to set file permissions with chmod");
        return false;
    }

    return true;
}

/**
 * @brief Helper function to run a single test case.
 */
void runTest(const char* filename, const char* testName) {
    printf("\n%s\n", testName);
    if (createAndMakeExecutable(filename)) {
        printf("-> Success: Created and made '%s' executable.\n", filename);
        // Verification
        struct stat st;
        if (stat(filename, &st) == 0) {
            if (st.st_mode & S_IXUSR) {
                printf("-> Verification: File is executable by owner.\n");
            } else {
                printf("-> Verification: File is NOT executable by owner.\n");
            }
        } else {
            perror("-> Verification failed (stat)");
        }
    } else {
        printf("-> Failure: Could not create and make '%s' executable.\n", filename);
    }
}

int main() {
    const char* filename = "script.sh";
    printf("--- C Test Cases ---\n");
    
    // Test Case 1: Initial creation
    runTest(filename, "Test Case 1: Initial creation of script.sh");

    // Test Case 2: Overwriting the existing file
    runTest(filename, "Test Case 2: Overwriting script.sh");

    // Test Case 3: Deleting the file to test re-creation
    printf("\nTest Case 3: Deleting the file\n");
    if (remove(filename) == 0) {
        printf("-> Success: File '%s' deleted.\n", filename);
    } else {
        perror("-> Failure: Could not delete file");
    }

    // Test Case 4: Re-creating the file after deletion
    runTest(filename, "Test Case 4: Re-creating the file script.sh");

    // Test Case 5: Final cleanup
    printf("\nTest Case 5: Final cleanup\n");
    if (remove(filename) == 0) {
        printf("-> Success: Cleanup complete.\n");
    } else {
        // It might have already been deleted or failed to create, so non-zero is not always an error here.
        // We use perror for consistency if an actual error occurred.
        // perror("-> Cleanup: Could not remove file (might not exist)");
    }
    
    return 0;
}