// Note: This code requires a C++17 compliant compiler and must be linked
// with the appropriate filesystem library if necessary (e.g., -lstdc++fs for older GCC).
// Compile with a command like: g++ -std=c++17 your_file.cpp -o your_program
#include <iostream>
#include <fstream>
#include <string>
#include <filesystem>
#include <system_error>

// Alias for convenience
namespace fs = std::filesystem;

/**
 * @brief Creates a file, writes content, and makes it executable for the owner.
 *
 * This function creates a file with the specified name, writes a simple shell
 * script content to it, and then adds the owner-execute permission to the file.
 * This is primarily intended for POSIX-like systems.
 *
 * @param filename The path to the file to be created.
 * @return true on success, false on failure.
 */
bool createAndMakeExecutable(const std::string& filename) {
    // A simple, safe script content.
    const std::string content = "#!/bin/bash\n"
                                "# A simple script generated by a C++ program.\n"
                                "echo \"Hello from script.sh! Execution successful.\"\n";

    // Step 1: Create and write to the file.
    // The ofstream constructor opens the file for writing, creating it if it doesn't exist.
    std::ofstream script_file(filename);
    if (!script_file) {
        std::cerr << "Error: Failed to open or create file '" << filename << "' for writing." << std::endl;
        return false;
    }
    script_file << content;
    script_file.close();

    // Step 2: Make the file executable.
    try {
        // Get current permissions.
        auto perms = fs::status(filename).permissions();
        // Add execute permission for the owner.
        fs::permissions(filename, perms | fs::perms::owner_exec, fs::perm_options::replace);
    } catch (const std::system_error& e) {
        std::cerr << "Error: Failed to set file permissions. " << e.what() << std::endl;
        return false;
    }

    return true;
}

/**
 * @brief Helper function to run a single test case.
 */
void runTest(const std::string& filename, const std::string& testName) {
    std::cout << "\n" << testName << std::endl;
    if (createAndMakeExecutable(filename)) {
        std::cout << "-> Success: Created and made '" << filename << "' executable." << std::endl;
        // Verification
        try {
            auto perms = fs::status(filename).permissions();
            if ((perms & fs::perms::owner_exec) != fs::perms::none) {
                std::cout << "-> Verification: File is executable by owner." << std::endl;
            } else {
                std::cout << "-> Verification: File is NOT executable by owner." << std::endl;
            }
        } catch (const std::system_error& e) {
            std::cerr << "-> Verification failed: " << e.what() << std::endl;
        }
    } else {
        std::cout << "-> Failure: Could not create and make '" << filename << "' executable." << std::endl;
    }
}

int main() {
    const std::string filename = "script.sh";
    std::cout << "--- C++ Test Cases ---" << std::endl;

    // Test Case 1: Initial creation
    runTest(filename, "Test Case 1: Initial creation of " + filename);

    // Test Case 2: Overwriting the existing file
    runTest(filename, "Test Case 2: Overwriting " + filename);

    // Test Case 3: Deleting the file to test re-creation
    std::cout << "\nTest Case 3: Deleting the file" << std::endl;
    try {
        if (fs::remove(filename)) {
            std::cout << "-> Success: File '" << filename << "' deleted." << std::endl;
        }
    } catch (const fs::filesystem_error& e) {
        std::cerr << "-> Failure: Could not delete file: " << e.what() << std::endl;
    }

    // Test Case 4: Re-creating the file after deletion
    runTest(filename, "Test Case 4: Re-creating the file " + filename);

    // Test Case 5: Final cleanup
    std::cout << "\nTest Case 5: Final cleanup" << std::endl;
    try {
        if (fs::exists(filename)) {
            fs::remove(filename);
            std::cout << "-> Success: Cleanup complete." << std::endl;
        }
    } catch (const fs::filesystem_error& e) {
        std::cerr << "-> Failure: Cleanup failed: " << e.what() << std::endl;
    }

    return 0;
}